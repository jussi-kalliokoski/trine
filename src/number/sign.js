"use strict";

/**
 * Returns the sign of the number, indicating whether the number is positive, negative or zero.
 *
 * @this {number}
 * @example Basic Usage
 *
 * ```javascript
 * 0::sign() // returns 0
 * 4::sign() // returns 1
 * -5::sign() // returns -1
 * ```
*/
export function sign (

) : number {
    return Math.sign(this);
};
